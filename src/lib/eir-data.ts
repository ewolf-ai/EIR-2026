// Función helper para obtener la comunidad de una provincia
export function getCommunityFromProvince(province: string): string | undefined {
  const mapping = PROVINCE_TO_COMMUNITY.find(
    p => p.province.toUpperCase() === province.toUpperCase()
  );
  return mapping?.community;
}

// Función helper para obtener provincias de una comunidad
export function getProvincesFromCommunity(community: string): string[] {
  return PROVINCE_TO_COMMUNITY
    .filter(p => p.community === community)
    .map(p => p.province);
}
